function randomizeValues() {
    event.preventDefault();
    Object.keys(sliders).forEach(key => {
        
        const minEntry = minValues.find(val => val.startsWith(key));
        const maxEntry = maxValues.find(val => val.startsWith(key));

        if (minEntry && maxEntry) {
            const min = parseInt(minEntry.split(',')[1].trim());
            const max = parseInt(maxEntry.split(',')[1].trim());
            const randomValue = Math.floor(Math.random() * (max - min + 1)) + min;
            sliders[key] = randomValue;
            document.getElementById(key).value = randomValue;
            document.getElementById(key + '_value').textContent = randomValue;
        } else {
            console.error(`No matching min or max value found for key: ${key}`);
        }
    });

    outputValues(); // Update output after randomization
}


