// Function to parse the data and create slider elements
function createSliders(minValues, maxValues) {
    const slidersContainer = document.getElementById('sliders');
    minValues.forEach((minValue, index) => {
        let [name, min] = minValue.split(',');
        let [, max] = maxValues[index].split(',');

        min = parseInt(min.trim());
        max = parseInt(max.trim());

        // Swap values if min is greater than max
        if (min > max) {
            [min, max] = [max, min];
        }

        // Create slider element
        const sliderContainer = document.createElement('div');
        sliderContainer.className = 'slider-container';

        const label = document.createElement('label');
        label.for = name;
        label.textContent = name;

        const input = document.createElement('input');
        input.type = 'range';
        input.id = name;
        input.name = name;
        input.min = min;
        input.max = max;
        input.value = min; // Set initial value here

        const valueDisplay = document.createElement('span');
        valueDisplay.id = name + '_value';
        valueDisplay.textContent = min; // Initial display value

        input.addEventListener('input', (event) => {
            valueDisplay.textContent = event.target.value;
            sliders[event.target.name] = parseInt(event.target.value);
        });

        // Store initial value directly in sliders object
        sliders[name] = min;

        sliderContainer.appendChild(label);
        sliderContainer.appendChild(input);
        sliderContainer.appendChild(valueDisplay);
        slidersContainer.appendChild(sliderContainer);
    });
}

